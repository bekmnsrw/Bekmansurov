    @SerialName("shortDescription") val shortDescription: String?,
    @SerialName("slogan") val slogan: String?,
    @SerialName("ratingMpaa") val ratingMpaa: String?,
    @SerialName("ratingFilmCritics") val ratingFilmCritics: Double?,
